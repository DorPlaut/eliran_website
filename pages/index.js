import Head from 'next/head';
import Image from 'next/image';
import Loader from '@/components/Loader';

import Header from '@/components/Header';
import Hero from '@/components/Hero';
import Footer from '@/components/Footer';
import ServicesIcons from '@/components/ServicesIcons';
import ShortPost from '@/components/ShortPost';
// states
import { useMobileContext } from '@/context/mobile';
import { usePostsContext } from '@/context/posts';
import { useEffect, useState } from 'react';

// localezation
import { useTranslation } from 'next-i18next';
import { serverSideTranslations } from 'next-i18next/serverSideTranslations';
import { useRouter } from 'next/router';
import Link from 'next/link';

export default function Home() {
  // states
  const [isMobile, setIsMobile] = useMobileContext();
  const [posts, setPosts] = usePostsContext();
  const [selectedPosts, setSelected] = useState();
  const [aboutPage, setAboutPage] = useState();
  // localezation
  const { locale = 'he', locales, push } = useRouter();
  const { t: translate } = useTranslation('home');
  // flitersd unwonted posts
  useEffect(() => {
    if (posts) {
      setAboutPage(
        posts.filter((i) => {
          return i.type == 'אודות';
        })
      );
      setSelected(
        posts.filter((i) => {
          return i.type == 'כתבה';
        })
      );
    }
  }, [posts]);
  return (
    <>
      <Head>
        <title>עורך דין אלירן בללי</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="main">
        <header className="header" id="top">
          <Header translate={translate} locales={locales} />
        </header>
        {selectedPosts && aboutPage ? (
          <>
            <Hero aboutPage={aboutPage} />
            <ShortPost flipped post={selectedPosts[0]} />
            <ServicesIcons />
            <ShortPost flipped post={selectedPosts[0]} />
          </>
        ) : (
          <section>
            <Loader />
          </section>
        )}
        <footer>
          <Footer translate={translate} />
        </footer>
      </main>
    </>
  );
}

export async function getStaticProps({ locale }) {
  return {
    props: {
      ...(await serverSideTranslations(locale, ['common', 'home'])),
      // Will be passed to the page component as props
    },
  };
}
